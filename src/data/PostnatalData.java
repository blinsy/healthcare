package data;

public class PostnatalData {
	private String Header;
	private String Description;

	public PostnatalData
	(
			String Header,
			String Description
	)
	{
		this.Header=Header;
		this.Description=Description;
	}
	public String getHeader() {
		return Header;
	}
	public String getDescription(){
		return Description;
	}


	}

