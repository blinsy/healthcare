package data;

public class AilementData {

	private String Header;
	private String Description;
	
	public AilementData
	
	(
		String Header,
		String Description
	)
	
	
	{
		
		this.Header=Header;
		this.Description=Description;
	}
	
	public String getHeader (){
		return Header;
	}
	public String getDescription(){
		return Description;
	}
}
